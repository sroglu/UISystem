namespace mehmetsrl.UISystem.Enums
{
    /// <summary>
    /// M3 button size scale.
    /// Heights and padding match the M3 spec density ramp.
    /// </summary>
    public enum ButtonSize
    {
        ExtraSmall,  // 24dp height, 12dp horiz padding
        Small,       // 32dp height, 16dp horiz padding
        Medium,      // 40dp height, 24dp horiz padding  (default)
        Large,       // 56dp height, 32dp horiz padding
        ExtraLarge,  // 96dp height, 48dp horiz padding
    }
}
